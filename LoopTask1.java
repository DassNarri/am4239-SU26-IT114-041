public class LoopTask1
{
    public static void main(String[] args) 
    {
        for(int i = 1; i < 6; i++)
        {
            System.out.println(i);
        }

        System.out.println();

        int a = 0;
        while(a < 5)
        {
            a++;
            System.out.println(a);
        }
    }
}